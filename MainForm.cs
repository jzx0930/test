        private void checkLaserStatus()
        {
            if (_Laser.GetState().Equals(SteadyState.Ready) && !_isLaserBusy)
            {
                // Get Laser status from Laser
                try
                {
                    _isLaserBusy = true;
                    _Laser_Status = _Laser.GetLaserStatus();
                    _isLaserBusy = false;
                }
                catch (Exception)
                {
                    writeError("MonitorError", "雷射系統異常.");
                    setMonitorStatusFail();

                    //MES 故障狀態
                    if (_ConfigSystem._ConnectSetting.ChptMES && SendErr_ToMES_Req)
                    {
                        SendErr_ToMES_Req = false;
                        Description = "故障與維修";
                        //[BM:將機台資訊傳送給MES]
                        _CHPT_MES.Send_MES_Data(EquipmentNo, (int)EquipmentState.Error, UserNo, Description, IsEQPTrigger, _CHPT_MES.Batch_Number);
                    }
                }

                // Check state changed or not
                if (_Laser_Status.Equals(_Laser_LastStatus))
                    return;

                // Check state is EmissionOn or not. If not, throw exception
                if (_Laser_Status.StateName != _Laser_LastStatus.StateName)
                {
                    if (_Laser_Status.StateName != "StateEmissionOn")
                    {
                        //MES 故障狀態
                        if (_ConfigSystem._ConnectSetting.ChptMES && SendErr_ToMES_Req)
                        {
                            SendErr_ToMES_Req = false;
                            Description = "故障與維修";
                            //[BM:將機台資訊傳送給MES]
                            _CHPT_MES.Send_MES_Data(EquipmentNo, (int)EquipmentState.Error, UserNo, Description, IsEQPTrigger, _CHPT_MES.Batch_Number);
                        }

                        if (BW_AutoProcess.IsBusy)//若該執行續有在運作
                            BW_AutoProcess.CancelAsync();// 外部請求 停止加工流程(執行續若有遇到檢查點時才會停止)

                        throw new Exception(string.Format("雷射激發失敗." + Environment.NewLine + "當前狀態名稱 = {0}", _Laser_Status.StateName));

                    }
                }

                _Laser_LastStatus = _Laser_Status;
            }
        }

