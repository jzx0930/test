        /// 根據模擬模式與系統設定，逐步啟用各模組功能並回報進度。
        /// </summary>
        /// <remarks>
        /// 此方法在系統啟動階段執行，負責初始化通訊模組、IO 控制、運動控制與自動製程模組，
        /// 並透過 <see cref="BW_SystemInitProcess.ReportProgress"/> 回報每個步驟的進度，
        /// 同時記錄初始化完成事件以供後續追蹤與診斷。
        /// <list type="bullet">
        ///   <item>若系統為模擬模式（<c>_isSysSimulated</c>），則跳過所有硬體初始化流程並啟用 UI 群組</item>
        ///   <item>初始化通訊模組（<c>commModuleInitialize</c>）以建立各設備的通訊連線</item>
        ///   <item>根據設定檔 <c>_ConfigSystem._ConnectSetting</c> 決定是否啟用 IO 控制與運動控制模組</item>
        ///   <item>啟用自動製程模組（<c>AutoProcess_Initialize</c>）以支援後續加工流程</item>
        ///   <item>每個初始化步驟皆透過 <c>ReportProgress</c> 回報進度並暫停 500 毫秒以穩定流程</item>
        ///   <item>最後透過 <see cref="writeEvent"/> 方法記錄初始化完成事件</item>
        /// </list>
        /// 此方法為系統啟動的核心流程之一，確保各模組在進入操作階段前已正確啟用與配置。
        /// </remarks>

        private void initialization()
        {
            //Skip if isSimulated
            if (_isSysSimulated)
            {
                enableUIGroup(true);
                return;
            }


            // Initialize commModule function 
            //[BM-溫溼度0]
            this.commModuleInitialize();
            BW_SystemInitProcess.ReportProgress(0, "CommModule initialized");
            Thread.Sleep(500);

            // Initialize IOControl function which located in MainForm.IOControl.cs
            if (_ConfigSystem._ConnectSetting.IOCard)
            {
                this.IOControl_Initialize();
                BW_SystemInitProcess.ReportProgress(0, "IOControl initialized");
                Thread.Sleep(500);
            }

            // Initialize MotionControl function which located in MainForm.MotionControl.cs
            if (_ConfigSystem._ConnectSetting.MotionControl)
            {
                this.MotionControl_Initialize();
                BW_SystemInitProcess.ReportProgress(0, "MotionControl initialized");
                Thread.Sleep(500);
            }

            // Initialize AutoProcess function which located in MainForm.AutoProcess.cs
            this.AutoProcess_Initialize();
            BW_SystemInitProcess.ReportProgress(0, "AutoProcess initialized");
            Thread.Sleep(500);


            // Write Event Log
            writeEvent("MainForm", "Initialization completed!");
        }
        /// <summary>
        /// 系統關閉或重啟前的資源釋放流程，
        /// 負責停用運動控制模組、通訊模組與所有定時器，並重設系統初始化狀態。
        /// </summary>
        /// <remarks>
        /// 此方法在系統結束階段執行，確保所有模組與背景資源安全關閉，
        /// 以避免設備持續運作或資源未釋放造成錯誤。
        ///
        /// 執行順序如下：
        /// <list type="bullet">
        ///   <item>呼叫 <see cref="MotionControl_Deinitialize"/> 停用運動控制模組</item>
        ///   <item>若系統為模擬模式（<c>_isSysSimulated</c>），則跳過硬體資源釋放</item>
        ///   <item>停止所有定時器，包括 <c>_timer1000</c>、<c>_timerComm</c>、<c>_timerLog</c> 與 <c>Timer100</c></item>
        ///   <item>呼叫 <see cref="commModuleDeinitialize"/> 停用所有通訊模組</item>
        ///   <item>將系統初始化旗標 <c>_isSysInitialized</c> 設為 false，表示系統已解除初始化</item>
        /// </list>
        ///
        /// 此方法為系統資源管理的關鍵一環，確保模組在關閉或重啟前處於安全狀態，
        /// 並避免殘留連線或定時任務造成系統異常。
        /// </remarks>

        private void deinitialization()
        {
            // Deinitialize MotionControl function
            this.MotionControl_Deinitialize();

            //Skip if isSimulated
            if (_isSysSimulated)
                return;

            // Switch Off Timer
            _timer1000.Stop();
            _timerComm.Stop();
            _timerLog.Stop();

            // Switch Off Timer100 operation
            Timer100.Stop();

            // Deinitialize commModule function
            this.commModuleDeinitialize();
            _isSysInitialized = false;
        }
