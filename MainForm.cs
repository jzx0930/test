        /// <summary>
        /// 用於封裝更新控制項文字屬性的委派方法定義，
        /// 搭配 Invoke 機制使 UI 元件能在非 UI 執行緒安全地修改 Text 屬性。
        /// </summary>
        private delegate void dUpdateControlWithString(Control control, string text);
        /// <summary>
        /// 安全更新指定 WinForms 控制項的文字內容，支援跨執行緒操作，
        /// 若呼叫來源非 UI 執行緒，則透過 <see cref="Control.Invoke"/> 將委派方法執行於 UI 執行緒以確保 Thread-safe。
        /// 適用於背景執行環境（如 Task、Thread、BackgroundWorker）中的 UI Text 更新場景。
        /// </summary>
        /// <param name="control">
        /// 欲更新的目標 UI 控制項，通常為 <c>Label</c>、<c>TextBox</c>、<c>GroupBox</c> 等含 <c>Text</c> 屬性之元件。</param>
        /// <param name="text">
        /// 要顯示的文字內容，可包含格式化字串、機台座標或狀態提示資訊。</param>
        /// <remarks>
        /// 方法流程如下：
        /// <list type="bullet">
        ///   <item>判斷呼叫端是否來自 UI 執行緒（<see cref="Control.InvokeRequired"/>）</item>
        ///   <item>若需跨執行緒操作，則建立 <see cref="dUpdateControlWithString"/> 委派並透過 <c>Invoke</c> 回 UI 執行緒更新</item>
        ///   <item>若已在 UI 執行緒，則直接設定 <c>control.Text</c> 屬性</item>
        /// </list>
        /// 此方法常與 Auto-mode 初始化流程搭配，確保 UI 元件能在背景流程中即時顯示狀態資訊，
        /// 且避免 <c>Cross-thread operation not valid</c> 例外發生。
        /// </remarks>
        private void updateControlTxtWithString(Control control, string text)
        {
            if (control.InvokeRequired)
            {
                var func = new dUpdateControlWithString(updateControlTxtWithString);
                control.Invoke(func, control, text);
            }
            else
            {
                control.Text = text;
            }
        }

