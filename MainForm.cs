        /// <summary>
        /// 急停內容
        /// </summary>
        private void setEmergencyStopLock()
        {
            // Check already happened or not
            if (_isSysEmergencyStop)
                return;

            // Emergency Stop pressed
            _isSysEmergencyStop = true;
            updatePnlStepState(pnl_System_EmergencyStop_Status, StepState.NO);

            // Interlock tripped
            //setInterlockBehavior();

            if (_isSysInterlocked)
                return;

            // Interlock trip happened
            setSystemInterlock(true);

            if (_isSysSimulated)
                return;

            // Disable UI control
            enableUIGroup(false);

            // Cancel processing
            if (BW_ThickProcess.IsBusy)
                BW_ThickProcess.CancelAsync();
            if (BW_AutoProcess.IsBusy)
                BW_AutoProcess.CancelAsync();
            if (BW_SampleMatrix.IsBusy)
                BW_SampleMatrix.CancelAsync();

            // Abort motion
            if (_MotionControl.GetState().Equals(SteadyState.Ready))
                abortMotion(_iMotionAxisAll);

            // Stop Scanner
            if (_LaserScanner.GetState().Equals(SteadyState.Ready))
                EmergencyStopLaserCutting();

            // Clear flag
            _isProcessing = false;

            _isLasing = false;

            //關閉氮氣
            enableProcessGas(false);

            //MES 故障狀態
            if (_ConfigSystem._ConnectSetting.ChptMES && SendErr_ToMES_Req)
            {
                SendErr_ToMES_Req = false;
                Description = "故障與維修";
                //[BM:將機台資訊傳送給MES]
                _CHPT_MES.Send_MES_Data(EquipmentNo, (int)EquipmentState.Error, UserNo, Description, IsEQPTrigger, _CHPT_MES.Batch_Number);
            }
        }

