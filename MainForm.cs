            }
            #endregion


            // Check Deinitialize and Disconnect
            bool flag = false;
            int count = 0;
            while (!flag)
            {
                // Assume Pass
                flag = true;

                foreach (var form in _moduleForms)
                {
                    if (form.Equals(_FormAutocorrelator))
                    {
                        if (!_Autocorrelator.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                    else if (form.Equals(_FormBeamProfiler))
                    {
                        // No corresponding implementation required
                    }
                    else if (form.Equals(_FormCameraCVX))
                    {
                        if (!_CameraCVX.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                    else if (form.Equals(_FormConfocalLaser))
                    {
                        if (!_ConfocalLaser.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                    else if (form.Equals(_FormIOCard))
                    {
                        if (!_IOCard.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                    else if (form.Equals(_FormLaser))
                    {
                        if (!_Laser.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                    else if (form.Equals(_FormLaserScanner))
                    {
                        if (!_LaserScanner.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                    else if (form.Equals(_FormPositionAligner))
                    {
                        if (!_PositionAligner.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                    else if (form.Equals(_FormPowermeter))
                    {
                        if (!_Powermeter.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                    else if (form.Equals(_FormRotationStage))
                    {
                        if (!_RotationStage.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                    else if (form.Equals(_FormTHSensor))
                    {
                        if (!_THSensor.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                    else if (form.Equals(_FormUPS))
                    {
                        if (!_UPS.GetState().Equals(SteadyState.Null))
                            flag = false;
                    }
                }
                count++;
                Thread.Sleep(100);
            }

            // Write Event
            writeEvent("Test", "Check Deinitialize and Disconnect done.");

            // Close CommModule
            foreach (var module in _CommModules)
            {
                module.Dispose();
            }

            // Close CommModule Form
            foreach (var form in _moduleForms)
            {
                form.Close();
            }

            // Write Event
            writeEvent("Test", "Module Forms Close done.");


            Thread.Sleep(1000);

            //MES ThreadSend Close
            _CHPT_MES.ThreadSend_MES_Close();

        }

