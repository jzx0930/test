        //[BM:自動刪除半年前的LOG檔案]
        /// <summary>
        /// TxtFileCleaner 類別用於清理指定資料夾中，檔名以日期開頭的 txt 檔案，
        /// 若檔案日期距離目前時間超過指定天數，則自動刪除。
        /// </summary>
        public class TxtFileCleaner // 定義一個名為 TxtFileCleaner 的類別，用來清理過期的 txt 檔案
        {
            private readonly string folderPath; // 欲掃描的資料夾路徑，從建構子傳入
            private readonly int daysThreshold; // 判斷是否過期的天數門檻，預設為 30 天

            /// <summary>
            /// 建構子：初始化 TxtFileCleaner 類別，設定資料夾路徑與天數門檻。
            /// </summary>
            /// <param name="folderPath">欲掃描的資料夾路徑</param>
            /// <param name="daysThreshold">距離目前時間的天數門檻，超過則刪除（預設為 30）</param>
            public TxtFileCleaner(string folderPath, int daysThreshold = 180)
            {
                this.folderPath = folderPath; // 將傳入的資料夾路徑儲存到欄位
                this.daysThreshold = daysThreshold; // 將傳入的天數門檻儲存到欄位，預設為 180半年
            }

            /// <summary>
            /// 執行清理流程：掃描資料夾中的 txt 檔案，判斷是否過期並刪除。
            /// 檔名格式需為 yyyyMMdd_開頭，例如 20250301_LCH_DataLog.txt。
            /// </summary>
            public void CleanOldTxtFiles()
            {
                if (!Directory.Exists(folderPath)) // 檢查指定的資料夾是否存在
                {
                    Console.WriteLine($"資料夾不存在：{folderPath}"); // 若不存在，輸出錯誤訊息
                    return; // 結束方法執行
                }

                string[] txtFiles = Directory.GetFiles(folderPath, "*.txt"); // 取得資料夾中所有副檔名為 .txt 的檔案

                foreach (var file in txtFiles) // 逐一處理每個 txt 檔案
                {
                    string fileName = Path.GetFileNameWithoutExtension(file); // 取得檔案名稱（不含副檔名）

                    // 使用正則表達式擷取檔名開頭的日期部分（格式為 yyyyMMdd）
                    Match match = Regex.Match(fileName, @"^(?<date>\d{8})_"); // 只抓取開頭 8 位數字後接底線的格式

                    if (match.Success) // 如果成功擷取到日期
                    {
                        string datePart = match.Groups["date"].Value; // 取得擷取到的日期字串（例如 "20250301"）

                        // 嘗試將字串轉換為 DateTime 物件，使用指定格式 yyyyMMdd
                        if (DateTime.TryParseExact(datePart, "yyyyMMdd", null, System.Globalization.DateTimeStyles.None, out DateTime fileDate))
                        {
                            TimeSpan diff = DateTime.Now - fileDate; // 計算檔案日期與目前時間的差距

                            if (diff.TotalDays > daysThreshold) // 如果差距超過指定天數門檻
                            {
                                try
                                {
                                    File.Delete(file); // 刪除該檔案
                                    Console.WriteLine($"已刪除：{fileName}.txt（距離 {diff.TotalDays:F0} 天）"); // 顯示刪除成功訊息與天數差距
                                }
                                catch (Exception ex) // 捕捉刪除失敗的例外狀況
                                {
                                    Console.WriteLine($"刪除失敗：{fileName}.txt，錯誤：{ex.Message}"); // 顯示錯誤訊息
                                }
                            }
                        }
                    }
                }
            }
        }

       
